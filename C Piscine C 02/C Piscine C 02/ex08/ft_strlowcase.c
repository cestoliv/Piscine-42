char	*ft_strlowcase(char *str)
{
	int	cur;

	cur = 0;
	while (str[cur] != 0)
	{
		if (str[cur] >= 65 && str[cur] <= 90)
		{
			str[cur] = str[cur] + 32;
		}
		cur++;
	}
	return (str);
}
