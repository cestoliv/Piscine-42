char	*ft_strupcase(char *str)
{
	int	cur;

	cur = 0;
	while (str[cur] != 0)
	{
		if (str[cur] >= 97 && str[cur] <= 122)
		{
			str[cur] = str[cur] - 32;
		}
		cur++;
	}
	return (str);
}
